
CREATE_PASSWORD:

	MOV COUNT9, #04H
	MOV DPTR,#PASSWORD
	MOV R0,#50H
	UP2:
	CLR A
	MOVC A, @A+DPTR
	MOV @R0, A
	INC R0
	INC DPTR
	DJNZ COUNT9, UP2
	MOV DPTR, #7001H
	MOV R0, #50H
	MOV COUNT9, #04H
	LCALL WRITE_DATA
	RET

READ_PASSWORD:
	MOV R1, #54H
	MOV DPTR, #7001H
	MOV COUNT9, #4H
	LCALL READ_DATA
	MOV COUNT9, #04H
	MOV R0, #54H		;STARTING TO DISPLAY THE PASSWORD ON THE LCD
	UP3:
	MOV A,@R0
	LCALL DISP
	INC R0
	DJNZ COUNT9, UP3
	RET

